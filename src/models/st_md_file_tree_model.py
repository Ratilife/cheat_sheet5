        # –í –µ–≥–æ –¥–∞–Ω–Ω—ã–µ –º—ã –∫–ª–∞–¥–µ–º –ø—É—Ç—å –∫ —Ñ–∞–π–ª—É, —á—Ç–æ–±—ã –ø–æ—Ç–æ–º –µ–≥–æ –º–æ–∂–Ω–æ –±—ã–ª–æ –æ—Ç–∫—Ä—ã—Ç—å.
        file_item = STMDFileTreeItem([root_name, root_type, file_path], self.root_item)

        # 4.–í—ã—á–∏—Å–ª—è–µ–º –ø–æ–∑–∏—Ü–∏—é, –∫—É–¥–∞ –±—É–¥–µ—Ç –≤—Å—Ç–∞–≤–ª–µ–Ω –Ω–æ–≤—ã–π —ç–ª–µ–º–µ–Ω—Ç (–≤ –∫–æ–Ω–µ—Ü —Å–ø–∏—Å–∫–∞ –¥–µ—Ç–µ–π –∫–æ—Ä–Ω—è)
