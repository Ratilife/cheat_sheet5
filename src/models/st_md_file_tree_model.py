        if file_path.endswith('.st'):
            root_type = "file"
        elif file_path.endswith('.md'):
            root_type = "markdown"
        else:
            root_type = "file"  # fallback

        # 3. –°–æ–∑–¥–∞–µ–º –∫–æ—Ä–Ω–µ–≤–æ–π —ç–ª–µ–º–µ–Ω—Ç –¥–ª—è –≠–¢–û–ì–û –§–ê–ô–õ–ê.
