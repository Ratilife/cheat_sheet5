        parent = item.parent_item

        # –ü–æ–¥–Ω–∏–º–∞–µ–º—Å—è –ø–æ –∏–µ—Ä–∞—Ä—Ö–∏–∏ —Ä–æ–¥–∏—Ç–µ–ª–µ–π:
        # - –ü–æ–∫–∞ –µ—Å—Ç—å —Ä–æ–¥–∏—Ç–µ–ª—å –∏ —ç—Ç–æ –Ω–µ –∫–æ—Ä–Ω–µ–≤–æ–π —ç–ª–µ–º–µ–Ω—Ç
        while parent and parent != self.root_item:
            # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —É—Ä–æ–≤–µ–Ω—å –≤–ª–æ–∂–µ–Ω–Ω–æ—Å—Ç–∏
            level += 1
